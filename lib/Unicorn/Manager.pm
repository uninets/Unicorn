=head1 NAME

Unicorn::Manager - A Perl interface to the Unicorn webserver

=head1 WARNING!

This is an unstable development release not ready for production!

=head1 VERSION

Version 0.006009

=head1 SYNOPSIS

The Unicorn::Manager distribution provides modules to manager the Unicorn webserver.

For the documentation see the modules perldocs.

L<Unicorn::Manager::CLI> is the command line interface to Unicorn using /proc
L<Unicorn::Manager::Server> is a not quiet complete server with a JSON api to query information

L<uc.pl> is a script to manage Unicorns
L<ucd.pl> is a script that spawns Unicorn::Manager::Server


